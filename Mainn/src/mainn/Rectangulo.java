package mainn;

public class Rectangulo {
    
    
    double area(double base, double altura)
    {
        return base * altura;
    }
    
    int area(int base, int altura)
    {
        return base * altura;
    }
    
    double perimetro(double base, double altura)
    {
        return 2 * base + 2 * altura;
    }
    
    int perimetro(int base, int altura)
    {
        return 2 * base + 2 * altura;
    }
    
    
}
